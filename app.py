@app.route('/chat', methods=['POST'])
def chat():
    try:
        data = request.json
        if not data or 'message' not in data:
            return jsonify({"response": "Invalid request. Please provide a 'message' field."})

        user_message = data['message']
        mode = data.get('mode', 'normal')

        if not user_message.strip():
            return jsonify({"response": "Please enter a valid message."})

        if not is_programming_related(user_message):
            return jsonify({
                "response": "I'm here to help with programming and coding questions! If you have any coding problems or need help with debugging, feel free to ask."
            })

        # Update system prompt based on mode
        system_message = {
            'normal': "You are a professional programming mentor. Only respond to programming-related questions.",
            'eli5': "You are explaining programming concepts in the simplest terms possible, as if explaining to a 5-year-old. Only respond to programming-related questions.",
            'debug': "You are a debugging expert. Only respond to programming-related questions.",
            'expert': "You are an advanced programming mentor. Only respond to programming-related questions."
        }.get(mode, "You are a helpful programming mentor. Only respond to programming-related questions.")

        # Prepare the messages for the Groq API
        messages = [
            {"role": "system", "content": system_message},
            {"role": "user", "content": user_message}
        ]

        # Generate response using Groq API
        response = llm.invoke(messages)

        # Extract the content from the AIMessage object
        response_content = response.content if hasattr(response, 'content') else str(response)

        # Format the response
        formatted_response = format_response(response_content, mode)

        # Update user progress
        user_data['experience_points'] += 10

        return jsonify({
            "response": formatted_response,
            "stats": user_data
        })

    except Exception as e:
        print(f"Error: {str(e)}")
        return jsonify({"response": f"An error occurred: {str(e)}. Please try again."})

