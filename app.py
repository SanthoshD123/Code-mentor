def format_response(response_content, mode):
    if mode == 'eli5':
        # Simplify the response for ELI5 mode
        response_content = re.sub(r'\*\*(.*?)\*\*', r'<strong>\1</strong>', response_content)
        response_content = re.sub(r'\*(.*?)\*', r'<em>\1</em>', response_content)
        response_content = re.sub(r'### (.*)', r'<h3> \1</h3>', response_content)
        response_content = re.sub(r'## (.*)', r'<h2> \1</h2>', response_content)
        response_content = re.sub(r'# (.*)', r'<h1> \1</h1>', response_content)
        response_content = re.sub(r'- (.*)', r'<li> \1</li>', response_content)
        response_content = response_content.replace('\n', '<br>')
        response_content = response_content.replace('code', ' code')
        response_content = response_content.replace('learn', ' learn')
        response_content = response_content.replace('fun', ' fun')
        response_content = response_content.replace('Python', ' Python')
    else:
        response_content = re.sub(r'\*\*(.*?)\*\*', r'<strong>\1</strong>', response_content)
        response_content = re.sub(r'\*(.*?)\*', r'<em>\1</em>', response_content)
        response_content = re.sub(r'### (.*)', r'<h3>\1</h3>', response_content)
        response_content = re.sub(r'## (.*)', r'<h2>\1</h2>', response_content)
        response_content = re.sub(r'# (.*)', r'<h1>\1</h1>', response_content)
        response_content = re.sub(r'- (.*)', r'<li>\1</li>', response_content)
        response_content = response_content.replace('\n', '<br>')
    return response_content

